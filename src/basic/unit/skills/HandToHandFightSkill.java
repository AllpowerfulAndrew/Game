package basic.unit.skills;

public class HandToHandFightSkill {
    private int handToHandFightSkill;
    private int handFightSkill;
    private int mainHandFightSkill;
    private int footFightSkill;
    private int mainFootFightSkill;

    public int getHandToHandFightSkill() {
        return handToHandFightSkill;
    }

    public void setHandToHandFightSkill(int handToHandFightSkill) {
        this.handToHandFightSkill = handToHandFightSkill;
    }

    public int getHandFightSkill() {
        return handFightSkill;
    }

    public void setHandFightSkill(int handFightSkill) {
        this.handFightSkill = handFightSkill;
    }

    public int getMainHandFightSkill() {
        return mainHandFightSkill;
    }

    public void setMainHandFightSkill(int mainHandFightSkill) {
        this.mainHandFightSkill = mainHandFightSkill;
    }

    public int getFootFightSkill() {
        return footFightSkill;
    }

    public void setFootFightSkill(int footFightSkill) {
        this.footFightSkill = footFightSkill;
    }

    public int getMainFootFightSkill() {
        return mainFootFightSkill;
    }

    public void setMainFootFightSkill(int mainFootFightSkill) {
        this.mainFootFightSkill = mainFootFightSkill;
    }
}