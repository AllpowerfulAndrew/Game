package basic.unit.skills;

public class OneHandedWeaponSkill {
    private int oneHandedWeaponFightSkill;
    private int knifeSkill;
    private int swordSkill;
    private int axeSkill;
    private int maceSkill;


    public int getOneHandedWeaponFightSkill() {
        return oneHandedWeaponFightSkill;
    }

    public void setOneHandedWeaponFightSkill(int oneHandedWeaponFightSkill) {
        this.oneHandedWeaponFightSkill = oneHandedWeaponFightSkill;
    }

    public int getKnifeSkill() {
        return knifeSkill;
    }

    public void setKnifeSkill(int knifeSkill) {
        this.knifeSkill = knifeSkill;
    }

    public int getSwordSkill() {
        return swordSkill;
    }

    public void setSwordSkill(int swordSkill) {
        this.swordSkill = swordSkill;
    }

    public int getAxeSkill() {
        return axeSkill;
    }

    public void setAxeSkill(int axeSkill) {
        this.axeSkill = axeSkill;
    }

    public int getMaceSkill() {
        return maceSkill;
    }

    public void setMaceSkill(int maceSkill) {
        this.maceSkill = maceSkill;
    }
}
