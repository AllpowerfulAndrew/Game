package basic.unit.skills;

public class ArmorSkill {
    private int armorSkill;
    private int clothArmorSkill;
    private int leatherArmorSkill;
    private int chainArmorSkill;
    private int scalyArmorSkill;
    private int latArmorSkill;

    public int getArmorSkill() {
        return armorSkill;
    }

    public void setArmorSkill(int armorSkill) {
        this.armorSkill = armorSkill;
    }

    public int getClothArmorSkill() {
        return clothArmorSkill;
    }

    public void setClothArmorSkill(int clothArmorSkill) {
        this.clothArmorSkill = clothArmorSkill;
    }

    public int getLeatherArmorSkill() {
        return leatherArmorSkill;
    }

    public void setLeatherArmorSkill(int leatherArmorSkill) {
        this.leatherArmorSkill = leatherArmorSkill;
    }

    public int getChainArmorSkill() {
        return chainArmorSkill;
    }

    public void setChainArmorSkill(int chainArmorSkill) {
        this.chainArmorSkill = chainArmorSkill;
    }

    public int getScalyArmorSkill() {
        return scalyArmorSkill;
    }

    public void setScalyArmorSkill(int scalyArmorSkill) {
        this.scalyArmorSkill = scalyArmorSkill;
    }

    public int getLatArmorSkill() {
        return latArmorSkill;
    }

    public void setLatArmorSkill(int latArmorSkill) {
        this.latArmorSkill = latArmorSkill;
    }
}
