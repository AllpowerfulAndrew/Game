package basic.unit.skills;

public class ShieldBlockSkill {
    private int shieldBlockSkill;
    private int lightShieldBlockSkill;
    private int middleShieldBlockSkill;
    private int heavyShieldBlockSkill;

    public int getShieldBlockSkill() {
        return shieldBlockSkill;
    }

    public void setShieldBlockSkill(int shieldBlockSkill) {
        this.shieldBlockSkill = shieldBlockSkill;
    }

    public int getLightShieldBlockSkill() {
        return lightShieldBlockSkill;
    }

    public void setLightShieldBlockSkill(int lightShieldBlockSkill) {
        this.lightShieldBlockSkill = lightShieldBlockSkill;
    }

    public int getMiddleShieldBlockSkill() {
        return middleShieldBlockSkill;
    }

    public void setMiddleShieldBlockSkill(int middleShieldBlockSkill) {
        this.middleShieldBlockSkill = middleShieldBlockSkill;
    }

    public int getHeavyShieldBlockSkill() {
        return heavyShieldBlockSkill;
    }

    public void setHeavyShieldBlockSkill(int heavyShieldBlockSkill) {
        this.heavyShieldBlockSkill = heavyShieldBlockSkill;
    }
}
